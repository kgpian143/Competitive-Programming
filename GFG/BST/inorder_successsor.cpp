class Solution
{
public:
    void traversal( Node *root , vector<Node * > &v )
    {
        if( root == NULL )return  ;
        traversal( root->left , v  ) ;
        v.push_back(root) ;
        // roo
        traversal( root->right , v ) ;
    }
    void populateNext(Node *root)
    {
        //code here
        vector<Node *> v ;
        traversal( root , v ) ;
        for( int i = 0 ; i < v.size()- 1; i++ )
        {
            v[i]->next = v[i+1] ;
        }
    }
};
